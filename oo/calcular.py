class Calcular:
    def soma(x, y):
        return x + y
    
    def subtrai(x, y):
        return x - y
    
    def multiplica(x, y):
        return x * y
    
    def dividir(x, y):
        return x /  y



calculo = Calcular


print(calculo.soma(10, 8))
print(calculo.subtrai(10, 8))
print(calculo.multiplica(5, 5))
print(calculo.dividir(8, 2))    